// ‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó
// ‚ïë  CICLO DE 4 ESTADOS (1 clique = 1 mudan√ßa)                     ‚ïë
// ‚ïë  1. normal     ‚Üí Tamanho padr√£o (1200x800)                    ‚ïë
// ‚ïë  2. minimized  ‚Üí Tamanho reduzido (900x600)                   ‚ïë
// ‚ïë  3. extended   ‚Üí Segunda tela OU maximizado (sem 2¬™ tela)     ‚ïë
// ‚ïë  4. VOLTA ‚Üí normal (ciclo fecha)                              ‚ïë
// ‚ïö‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïù

let windowState = 'normal';
let savedNormalBounds = { x: 100, y: 100, width: 1200, height: 800 }; // Posi√ß√£o inicial
let mainWindow = null;
let isProcessing = false; // Prote√ß√£o contra cliques r√°pidos

function createWindow() {
  // Criar a janela do navegador
  mainWindow = new BrowserWindow({
    width: 1200,
    height: 800,
    minWidth: 800,
    minHeight: 600,
    webPreferences: {
      nodeIntegration: false,
      contextIsolation: true,
      enableRemoteModule: false,
      webSecurity: true,
      preload: path.join(__dirname, 'preload.js')
    },
    icon: path.join(__dirname, '../assets/icon.png'),
    frame: false,
    transparent: false,
    hasShadow: true,
    show: false,
    backgroundColor: '#1a5c3a'
  });

  // Carregar a aplica√ß√£o
  if (isDev) {
    mainWindow.loadURL('http://localhost:3000');
    mainWindow.webContents.openDevTools();
  } else {
    mainWindow.loadFile(path.join(__dirname, '../dist/index.html'));
  }

  // Remover menu nativo completamente
  mainWindow.setMenu(null);
  mainWindow.setMenuBarVisibility(false);
  mainWindow.setAutoHideMenuBar(true);

  // Mostrar a janela quando estiver pronta
  mainWindow.once('ready-to-show', () => {
    mainWindow.show();
  });

  // Eventos de maximiza√ß√£o/restaura√ß√£o
  mainWindow.on('maximize', () => {
    mainWindow.webContents.send('window-maximized');
  });

  mainWindow.on('unmaximize', () => {
    mainWindow.webContents.send('window-unmaximized');
  });

  // Configurar o menu da aplica√ß√£o
  const template = [
    {
      label: 'Arquivo',
      submenu: [
        {
          label: 'Sair',
          accelerator: 'CmdOrCtrl+Q',
          click: () => {
            app.quit();
          }
        }
      ]
    },
    {
      label: 'Editar',
      submenu: [
        { role: 'undo', label: 'Desfazer' },
        { role: 'redo', label: 'Refazer' },
        { type: 'separator' },
        { role: 'cut', label: 'Cortar' },
        { role: 'copy', label: 'Copiar' },
        { role: 'paste', label: 'Colar' }
      ]
    },
    {
      label: 'Visualizar',
      submenu: [
        { role: 'reload', label: 'Recarregar' },
        { role: 'forceReload', label: 'For√ßar Recarregamento' },
        { role: 'toggleDevTools', label: 'Ferramentas do Desenvolvedor' },
        { type: 'separator' },
        { role: 'resetZoom', label: 'Zoom Padr√£o' },
        { role: 'zoomIn', label: 'Aumentar Zoom' },
        { role: 'zoomOut', label: 'Diminuir Zoom' },
        { type: 'separator' },
        { role: 'togglefullscreen', label: 'Tela Cheia' }
      ]
    },
    {
      label: 'Janela',
      submenu: [
        { role: 'minimize', label: 'Minimizar' },
        { role: 'close', label: 'Fechar' }
      ]
    },
    {
      label: 'Ajuda',
      submenu: [
        {
          label: 'Sobre o Sistema de Cart√≥rio',
          click: () => {
            const { dialog } = require('electron');
            dialog.showMessageBox(mainWindow, {
              type: 'info',
              title: 'Sobre',
              message: 'Sistema de Cart√≥rio',
              detail: 'Sistema de gest√£o para cart√≥rios\nVers√£o 1.0.0'
            });
          }
        }
      ]
    }
  ];

  // Menu removido - controles customizados no React
  // const menu = Menu.buildFromTemplate(template);
  // Menu.setApplicationMenu(menu);

  return mainWindow;
}

// IPC Handlers - Registrados UMA √öNICA VEZ (fora do createWindow)
// Handler para obter o estado atual
ipcMain.handle('get-window-state', () => {
  console.log('üì° React solicitou estado atual:', windowState);
  return windowState;
});

ipcMain.on('window-minimize', () => {
  if (mainWindow) {
    mainWindow.minimize();
  }
});

ipcMain.on('window-maximize', () => {
  if (!mainWindow) {
    console.log('‚ùå Janela n√£o existe');
    return;
  }
  
  // ‚õî PROTE√á√ÉO: Bloquear cliques m√∫ltiplos r√°pidos
  if (isProcessing) {
    console.log('‚è∏Ô∏è  CLIQUE IGNORADO - Processamento em andamento...');
    return;
  }
  
  isProcessing = true;
  console.log('\n‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ');
  console.log('üñ±Ô∏è  CLIQUE √öNICO RECEBIDO');
  console.log('üìä Estado ANTES:', windowState);
  console.log('üìç savedNormalBounds atual:', savedNormalBounds);
  
  try {
    const displays = screen.getAllDisplays();
    const primaryDisplay = displays[0];
    const currentBounds = mainWindow.getBounds();
    
    console.log('üñ•Ô∏è  Monitores dispon√≠veis:', displays.length);
    
    // ‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó
    // ‚ïë  CICLO: Normal ‚Üí Reduzido ‚Üí Estendido ‚Üí Normal (repete)  ‚ïë
    // ‚ïö‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïù
    
    if (windowState === 'normal') {
      // ‚îÅ‚îÅ‚îÅ PASSO 1/4: Normal ‚Üí Reduzido ‚îÅ‚îÅ‚îÅ
      console.log('üîµ PASSO 1: Normal (1200x800) ‚Üí Reduzido (900x600)');
      
      // Salvar posi√ß√£o normal atual
      savedNormalBounds = { ...currentBounds };
      console.log('üíæ Posi√ß√£o normal salva:', savedNormalBounds);
      
      // Centralizar janela reduzida
      const centerX = primaryDisplay.bounds.x + (primaryDisplay.bounds.width - 900) / 2;
      const centerY = primaryDisplay.bounds.y + (primaryDisplay.bounds.height - 600) / 2;
      
      mainWindow.setBounds({
        x: Math.floor(centerX),
        y: Math.floor(centerY),
        width: 900,
        height: 600
      });
      
      // MUDAR ESTADO IMEDIATAMENTE
      windowState = 'minimized';
      mainWindow.webContents.send('window-state-changed', 'minimized');
      
      console.log('‚úÖ Mudou para: REDUZIDO (900x600)');
      
    } else if (windowState === 'minimized') {
      // ‚îÅ‚îÅ‚îÅ PASSO 2/4: Reduzido ‚Üí Estendido ‚îÅ‚îÅ‚îÅ
      console.log('üü¢ PASSO 2: Reduzido (900x600) ‚Üí Estendido (2¬™ tela OU maximizado)');
      
      // MUDAR ESTADO IMEDIATAMENTE
      windowState = 'extended';
      
      if (displays.length > 1) {
        // H√° segunda tela: estender para ela
        const secondDisplay = displays[1];
        console.log('üì∫ Estendendo para 2¬™ tela:', secondDisplay.bounds);
        
        mainWindow.setBounds({
          x: secondDisplay.bounds.x,
          y: secondDisplay.bounds.y,
          width: secondDisplay.bounds.width,
          height: secondDisplay.bounds.height
        });
        
        setTimeout(() => mainWindow.maximize(), 50);
        
      } else {
        // Sem segunda tela: maximizar na tela principal
        console.log('üì∫ Sem 2¬™ tela - Maximizando na tela principal');
        mainWindow.maximize();
      }
      
      mainWindow.webContents.send('window-state-changed', 'extended');
      console.log('‚úÖ Mudou para: ESTENDIDO');
      
    } else if (windowState === 'extended') {
      // ‚îÅ‚îÅ‚îÅ PASSO 3/4: Estendido ‚Üí Normal (FECHA O CICLO) ‚îÅ‚îÅ‚îÅ
      console.log('üü° PASSO 3: Estendido ‚Üí Normal (VOLTA AO IN√çCIO)');
      console.log('üîô Restaurando posi√ß√£o normal salva:', savedNormalBounds);
      
      mainWindow.unmaximize();
      
      // IMPORTANTE: Mudar estado IMEDIATAMENTE
      windowState = 'normal';
      
      // Validar savedNormalBounds
      const boundsToRestore = {
        x: savedNormalBounds.x || 100,
        y: savedNormalBounds.y || 100,
        width: savedNormalBounds.width || 1200,
        height: savedNormalBounds.height || 800
      };
      
      console.log('üîß Aplicando bounds:', boundsToRestore);
      
      setTimeout(() => {
        mainWindow.setBounds(boundsToRestore);
        mainWindow.webContents.send('window-state-changed', 'normal');
        console.log('‚úÖ Mudou para: NORMAL');
        console.log('üéâ CICLO COMPLETO! Estado atual:', windowState);
      }, 100);
      
    } else {
      // Estado inesperado - resetar para normal
      console.error('‚ùå ESTADO INESPERADO:', windowState);
      console.log('üîÑ Resetando para NORMAL...');
      windowState = 'normal';
      mainWindow.webContents.send('window-state-changed', 'normal');
    }
    
    console.log('üìä Estado DEPOIS:', windowState);
    console.log('‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ\n');
    
  } catch (error) {
    console.error('‚ùå ERRO no ciclo:', error);
    windowState = 'normal';
    mainWindow.webContents.send('window-state-changed', 'normal');
  } finally {
    // Liberar ap√≥s 250ms para evitar cliques duplos
    setTimeout(() => {
      isProcessing = false;
      console.log('üîì Pronto para pr√≥ximo clique\n');
    }, 250);
  }
});

ipcMain.on('window-close', () => {
  if (mainWindow) {
    mainWindow.close();
  }
});

// Este m√©todo ser√° chamado quando o Electron terminar de inicializar
app.whenReady().then(() => {
  createWindow();

  app.on('activate', () => {
    if (BrowserWindow.getAllWindows().length === 0) {
      createWindow();
    }
  });
});

// Sair quando todas as janelas estiverem fechadas
app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') {
    app.quit();
  }
});

// Configura√ß√µes de seguran√ßa
app.on('web-contents-created', (event, contents) => {
  contents.on('new-window', (event, navigationUrl) => {
    event.preventDefault();
  });
});
